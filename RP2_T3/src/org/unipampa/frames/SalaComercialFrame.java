    public int pegaCodigo(String codig) {
        int end = 0;
        for (int i = 0; i < codig.length(); i++) {
            if (codig.charAt(i) == '-') {
                end = i - 1;
                break;
            }
        }
        codig = codig.substring(0, end);
        return Integer.parseInt(codig);
    }

